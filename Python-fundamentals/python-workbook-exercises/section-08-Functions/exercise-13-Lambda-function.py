'''Q13:- Define a lambda function that takes two integers as input and returns their sum. 
Test the lambda function with different inputs.'''

# Defining a lambda function that takes two integers as input and returns their sum. 
lam = lambda a,b:a+b

# Testing the lambda function with different values
print(f"\nSum = {lam(3,2)}")

print(f"\nSum = {lam(5,2)}")

print(f"\nSum = {lam(7,3)}\n")