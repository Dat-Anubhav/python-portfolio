'''Q1:- Create a list of the first 20 positive integers. Print the list.'''

# Created an empty list
l=[]
for i in range(1,21):

    l.append(i) # The l.append() method adds a single item to the end of the list.
    
# Displaying the list of first 20 positive integers    
print("\nList of first 20 positive integers is = ",l)