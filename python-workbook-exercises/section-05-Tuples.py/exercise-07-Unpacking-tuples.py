'''Q7:- Create a tuple with 5 elements and unpack it into 5 variables. Print the variables.'''

t=(1,2,3,"four",5)

print(f"\nTuple of five elements = {t}\n")

# Unpacking tuple
a,b,c,d,e=t

# Displaying all five variables with their assigned tuple elements
print(f"\nFirst element = {a}\
\nSecond element = {b}\
\nThird element = {c}\
\nFourth element = {d}\
\nFifth element = {e}\n")